async def collect_source(role: str, src: str) -> Path | None:
    """Stage 1: собирает корпус для одного источника → raw_corpus.md."""
    artifacts_dir = f"./artifacts/test_{src}_only"
    service = ResearchIngestionService(artifacts_dir)
    specs = specs_for(role, src)
    print(f"  [{src}] collecting...")
    artifacts = await service.collect_sources(role, specs)
    if not artifacts:
        print(f"  [{src}] ⚠️ ничего не собрано")
        return None
    saved = await service.save_corpus(artifacts, role)
    print(f"  [{src}] ✅ {len(artifacts)} артефактов → {saved['corpus']}")
    return Path(saved["corpus"])


async def meta_one(role: str, src: str) -> Path | None:
    """Stage 2-meta: raw_corpus.md → context.md (мета-инфо роли).
    Параллельно с extract+clean. Использует свой META-промпт по источнику."""
    corpus_path = Path(f"artifacts/test_{src}_only/raw_corpus.md")
    if not corpus_path.exists():
        return None
    corpus = corpus_path.read_text(encoding="utf-8")
    meta_tpl = META_PROMPTS.get(src)
    if not meta_tpl:
        return None
    prompt = (meta_tpl
              .replace("{role}", role)
              .replace("{corpus}", corpus))
    print(f"  [{src}] meta: corpus={len(corpus)} chars")
    response = await call_llm(
        prompt,
        temperature=0.0,
        max_output_tokens=8000,
        streaming=False,
        system_message="META-экстрактор: извлекаешь только КОНТЕКСТ роли (условия, рынок, тренды), НЕ компетенции.",
    )
    if not response:
        print(f"  [{src}] ❌ meta пусто")
        return None
    out = Path(f"artifacts/extracted_md/{src}_context.md")
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(response, encoding="utf-8")
    print(f"  [{src}] ✅ meta → {out} ({len(response)} chars)")
    return out


async def extract_one(role: str, src: str) -> Path | None:
    """Stage 2: raw_corpus.md → черновой md (этап 1) → очищенный md (этап 2)."""
    corpus_path = Path(f"artifacts/test_{src}_only/raw_corpus.md")
    if not corpus_path.exists():
        print(f"  [{src}] ⚠️ нет raw_corpus.md")
        return None
    corpus = corpus_path.read_text(encoding="utf-8")

    extract_tpl, clean_tpl = PROMPTS[src]

    # Этап 1: максимальное извлечение (coverage > галлюцинации)
    extract_prompt = (extract_tpl
                      .replace("{role}", role)
                      .replace("{source}", src)
                      .replace("{description}", SOURCE_DESCRIPTIONS[src])
                      .replace("{corpus}", corpus))
    print(f"  [{src}] этап 1 — extract: corpus={len(corpus)} chars")
    draft = await call_llm(
        extract_prompt,
        temperature=0.0,
        max_output_tokens=8000,
        streaming=False,
        system_message="Этап 1: ЭКСТРАКТОР. Извлеки ВСЕ компетенции из источника, лучше с лишним.",
    )
    if not draft:
        print(f"  [{src}] ❌ этап 1 пусто")
        return None
    draft_path = Path(f"artifacts/extracted_md/{src}_draft.md")
    draft_path.parent.mkdir(parents=True, exist_ok=True)
    draft_path.write_text(draft, encoding="utf-8")
    print(f"  [{src}] этап 1 ✅ draft: {len(draft)} chars")

    # Этап 2: очистка от галлюцинаций
    clean_prompt = (clean_tpl
                    .replace("{draft}", draft)
                    .replace("{corpus}", corpus))
    print(f"  [{src}] этап 2 — clean")
    cleaned = await call_llm(
        clean_prompt,
        temperature=0.0,
        max_output_tokens=8000,
        streaming=False,
        system_message="Этап 2: ОЧИСТИТЕЛЬ. Удали из черновика всё чего нет в источнике, ничего не добавляй.",
    )
    if not cleaned:
        print(f"  [{src}] ⚠️ этап 2 пусто — оставляю draft")
        cleaned = draft

    out = Path(f"artifacts/extracted_md/{src}.md")
    out.write_text(cleaned, encoding="utf-8")
    print(f"  [{src}] этап 2 ✅ clean: {out} ({len(cleaned)} chars)")
    return out


async def main() -> None:
    parser = argparse.ArgumentParser(description="Pipeline: собрать корпусы → извлечь md-профили → merge → rank → consolidate")
    parser.add_argument("role", help="Целевая роль")
    parser.add_argument("--sources", nargs="+", default=DEFAULT_SOURCES,
                        choices=ALL_SOURCES, help="Источники (default: sonar hh telegram onet reddit)")
    parser.add_argument("--skip-collect", action="store_true",
                        help="Пропустить Stage 1 — использовать готовые raw_corpus.md")
    parser.add_argument("--full", action="store_true",
                        help="После Stage 2 прогнать Stage 3 (merge), Stage 4 (rank), Stage 5 (consolidate)")
    parser.add_argument("--only-post", action="store_true",
                        help="Пропустить Stage 1+2, запустить только Stage 3-5 (нужны готовые artifacts/extracted_md/*.md)")
    args = parser.parse_args()

    print(f"Роль: {args.role}")
    print(f"Источники: {args.sources}\n")

    if not args.only_post:
        if not args.skip_collect:
            print("=== Stage 1: сбор корпусов (параллельно) ===")
            await asyncio.gather(*[collect_source(args.role, s) for s in args.sources])

        print("\n=== Stage 2: извлечение компетенций + мета-контекста (параллельно) ===")
        tasks = []
        for s in args.sources:
            tasks.append(extract_one(args.role, s))
            tasks.append(meta_one(args.role, s))
        results = await asyncio.gather(*tasks)
        extract_results = results[0::2]
        meta_results = results[1::2]

        print("\n=== ИТОГ Stage 1+2 ===")
        for src, er, mr in zip(args.sources, extract_results, meta_results):
            print(f"  {src}:")
            print(f"    компетенции: {er if er else '❌'}")
            print(f"    контекст:    {mr if mr else '❌'}")

    if args.full or args.only_post:
        await run_post_stages(args.role)


async def run_post_stages(role: str) -> None:
    """Stage 3 (merge) → Stage 4 (rank). Консолидация отключена — сохраняем все 128 паспортов."""
    import subprocess
    import sys as _sys

    # Stage 3: merge passports
    print("\n=== Stage 3: merge 5 профилей → competency_passports.md ===")
    r = subprocess.run(
        [_sys.executable, "build_passports.py", "--role", role],
        cwd=Path(__file__).parent,
    )
    if r.returncode != 0:
        print("❌ Stage 3 провалился")
        return

    # Stage 4: rank competencies
    print("\n=== Stage 4: ранжирование паспортов (2 сигнала × батчинг) ===")
    r = subprocess.run(
        [_sys.executable, "rank_competencies.py", "--role", role],
        cwd=Path(__file__).parent,
    )
    if r.returncode != 0:
        print("❌ Stage 4 провалился")
        return

    print("\n=== ✓ ПАЙПЛАЙН ЗАВЕРШЁН ===")
    print("Финальный каталог: artifacts/extracted_md/competencies_ranked.md (все паспорта с Score/Tier)")


if __name__ == "__main__":
    asyncio.run(main())
